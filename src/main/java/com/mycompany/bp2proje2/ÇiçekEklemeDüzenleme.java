    private void TableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableMouseClicked
// Tıklanan satırın indeksi alınıyor
        int row = Table.getSelectedRow();

        if (row != -1) {
            // Seçili çiçeğin ID'si saklanıyor, güncelle/sil işlemlerinde kullanılacak
            seciliID = (int) Table.getValueAt(row, 0);

            // Tablodaki veriler forma aktarılıyor
            CicekAdiTextField.setText(Table.getValueAt(row, 1).toString());
            KategoriComboBox.setSelectedItem(Table.getValueAt(row, 2).toString());
            RenkComboBox.setSelectedItem(Table.getValueAt(row, 3).toString());
            FiyatSpinner.setValue(Table.getValueAt(row, 4));
            StokSpinner.setValue(Table.getValueAt(row, 5));

            // Tür bilgisine göre ilgili RadioButton seçili yapılıyor
            String tur = Table.getValueAt(row, 6).toString();
            if (tur.equals("Kesme")) {
                KesmeRadioButton.setSelected(true);
            } else if (tur.equals("Saksılı")) {
                SaksiliRadioButton.setSelected(true);
            } else if (tur.equals("Demet")) {
                DemetRadioButton.setSelected(true);
            }
        }

        // Veritabanı bağlantısı açılıyor
        Connection conn = getConnection();
        if (conn == null) {
            return;
        }

        try {
            Statement st = conn.createStatement();

            // Tabloda görünmeyen özellikler DB'den çekiliyor
            String sql = String.format("SELECT mevsimlik, ithal, kokulu, aciklama FROM cicekler WHERE id = %d", seciliID);
            ResultSet rs = st.executeQuery(sql);

            if (rs.next()) {
                // CheckBox ve TextArea değerleri DB'den gelen verilerle güncelleniyor
                MevsimlikCheckBox.setSelected(rs.getBoolean("mevsimlik"));
                IthalCheckBox.setSelected(rs.getBoolean("ithal"));
                KokuluCheckBox.setSelected(rs.getBoolean("kokulu"));
                AciklamaTextArea.setText(rs.getString("aciklama"));
            }
            st.close();
            conn.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Hata: " + ex.getMessage());
        }
    }//GEN-LAST:event_TableMouseClicked

    private void SilButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SilButtonActionPerformed
        // Tabloda seçili bir çiçek var mı kontrolü, -1 ise seçili yok demek
        if (seciliID == -1) {
            JOptionPane.showMessageDialog(null, "Lütfen bir çiçek seçiniz!", "Hata!", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Silme işlemi için kullanıcıdan onay alınıyor
        int confirm = JOptionPane.showConfirmDialog(null, "Bu çiçeği silmek istediğinize emin misiniz?", "Onay", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.NO_OPTION) {
            return;
        }

        // Veritabanı bağlantısı açılıyor
        Connection conn = getConnection();
        if (conn == null) {
            return;
        }

        try {
            Statement st = conn.createStatement();

            // Seçili çiçek veritabanından siliniyor
            String sql = String.format("DELETE FROM cicekler WHERE id=%d", seciliID);
            st.executeUpdate(sql);

            // Tablo güncelleniyor, seçili ID sıfırlanıyor ve form temizleniyor
            TabloYukle();
            seciliID = -1;
            CicekAdiTextField.setText("");
            FiyatSpinner.setValue(0.0);
            StokSpinner.setValue(0);
            AciklamaTextArea.setText("");
            MevsimlikCheckBox.setSelected(false);
            IthalCheckBox.setSelected(false);
            KokuluCheckBox.setSelected(false);

            st.close();
            conn.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Hata: " + ex.getMessage(), "Hata!", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_SilButtonActionPerformed

