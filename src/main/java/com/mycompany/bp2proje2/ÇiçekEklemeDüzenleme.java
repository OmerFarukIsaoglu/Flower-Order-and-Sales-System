     private void TabloYukle() {
// JTable için boş bir model oluşturuluyor ve sütunlar ekleniyor
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Ad");
        model.addColumn("Kategori");
        model.addColumn("Renk");
        model.addColumn("Fiyat");
        model.addColumn("Stok");
        model.addColumn("Tur");

        // Veritabanı bağlantısı açılıyor
        Connection conn = getConnection();
        if (conn == null) {
            return;
        }

        try {
            Statement st = conn.createStatement();

            // Tüm çiçekler veritabanından çekiliyor
            String sql = "SELECT * FROM cicekler";
            ResultSet rs = st.executeQuery(sql);

            // Her satır tabloya ekleniyor
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("ad"),
                    rs.getString("kategori"),
                    rs.getString("renk"),
                    rs.getInt("fiyat"),
                    rs.getInt("stok"),
                    rs.getString("tur")
                });
            }

            // Model JTable'a bağlanıyor
            Table.setModel(model);
            st.close();
            conn.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Tablo yükleme hatası: " + ex.getMessage(), "Hata!", JOptionPane.ERROR_MESSAGE);
        }
    }
