    private void EkleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EkleButtonActionPerformed
// Formdan çiçek bilgileri alınıyor
        String ad = CicekAdiTextField.getText().trim();
        String kategori = KategoriComboBox.getSelectedItem().toString();
        String renk = RenkComboBox.getSelectedItem().toString();
        int fiyat = (int) FiyatSpinner.getValue();
        int stok = (int) StokSpinner.getValue();
        String aciklama = AciklamaTextArea.getText().trim();

        // Seçili RadioButton'a göre tür belirleniyor
        String tur = "";
        if (KesmeRadioButton.isSelected()) {
            tur = "Kesme";
        } else if (SaksiliRadioButton.isSelected()) {
            tur = "Saksılı";
        } else if (DemetRadioButton.isSelected()) {
            tur = "Demet";
        }

        // CheckBox değerleri alınıyor
        boolean mevsimlik = MevsimlikCheckBox.isSelected();
        boolean ithal = IthalCheckBox.isSelected();
        boolean kokulu = KokuluCheckBox.isSelected();

        // Çiçek adı boş alan kontrolü
        if (ad.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Çiçek adı boş olamaz!", "Hata!", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Tür seçildi mi kontrolü
        if (tur.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Tür seçiniz!", "Hata!", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Çiçek adı regex kontrolü: sadece harf ve boşluk içermeli
        Pattern pattern = Pattern.compile("^[a-zA-ZğüşıöçĞÜŞİÖÇ ]+$");
        Matcher macther = pattern.matcher(ad);
        if (!macther.matches()) {
            JOptionPane.showMessageDialog(null, "Çiçek adı sadece harf içermelidir!", "Hata!", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Veritabanı bağlantısı açılıyor
        Connection conn = getConnection();
        if (conn == null) {
            return;
        }

        try {
            Statement st = conn.createStatement();

            // Yeni çiçek veritabanına ekleniyor
            String sql = String.format("INSERT INTO cicekler(ad, kategori, renk, fiyat, stok, tur, mevsimlik, ithal, kokulu, aciklama) VALUES ('%s', '%s', '%s', %d, %d, '%s', %b, %b, %b, '%s')",
                    ad, kategori, renk, fiyat, stok, tur, mevsimlik, ithal, kokulu, aciklama);
            st.executeUpdate(sql);

            JOptionPane.showMessageDialog(null, "Çiçek başarıyla eklendi!", "Başarılı", JOptionPane.INFORMATION_MESSAGE);

            // Tablo güncelleniyor ve form temizleniyor
            TabloYukle();
            CicekAdiTextField.setText("");
            FiyatSpinner.setValue(0);
            StokSpinner.setValue(0);
            AciklamaTextArea.setText("");
            MevsimlikCheckBox.setSelected(false);
            IthalCheckBox.setSelected(false);
            KokuluCheckBox.setSelected(false);

            st.close();
            conn.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Hata: " + ex.getMessage(), "Hata!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_EkleButtonActionPerformed

