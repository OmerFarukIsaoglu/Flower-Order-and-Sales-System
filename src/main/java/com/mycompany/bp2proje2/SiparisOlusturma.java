    private void CicekBilgileriniYukle() {
        // Veritabanı bağlantısı açılıyor
    Connection conn = getConnection();
    if (conn == null) {
        return;
    }
    
    try {
        Statement st = conn.createStatement();
        
        // Seçili çiçeğin bilgileri ID'ye göre veritabanından çekiliyor
        String sql = String.format("SELECT * FROM cicekler WHERE id=%d", seciliCicekId);
        ResultSet rs = st.executeQuery(sql);
        
        while (rs.next()) {
            // Renk ve kategori bilgisi birleştirilerek label'a yazılıyor
            RenkveKategoriLabel.setText(rs.getString("renk") + " " + rs.getString("kategori"));
            
            // Fiyat alınıyor ve ilgili label'lara yazılıyor
            cicekfiyati = rs.getInt("fiyat");
            FiyatLabel.setText(cicekfiyati + "₺");
            ToplamLabel.setText("Toplam: " + cicekfiyati + " ₺");
            
            // Stok bilgisine göre AdetSpinner'ın maksimum değeri ayarlanıyor
            // Kullanıcı stoktan fazla adet giremez
            int stok = rs.getInt("stok");
            AdetSpinner.setModel(new SpinnerNumberModel(1, 1, stok, 1));
        }
        st.close();
        conn.close();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "Hata: " + ex.getMessage());
    }
    }

