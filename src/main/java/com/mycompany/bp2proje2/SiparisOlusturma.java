    private void SipariTamamlaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SipariTamamlaButtonActionPerformed

        // Teslimat adresi, telefon, ödeme yöntemi ve hızlı teslimat bilgileri alınıyor
    String adres = TeslimatAdresiTextField.getText().trim();
    String telefon = TelefonTextField.getText().trim();
    String odeme = OdemeYontemiComboBox.getSelectedItem().toString();
    boolean hizliTeslimat = HizliTeslimatCheckBox.isSelected();
    
    // Sepet boş mu kontrolü
    if (model.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Sepet boş!", "Hata!", JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    // Veritabanı bağlantısı açılıyor
    Connection conn = getConnection();
    if (conn == null) {
        return;
    }
    
    try {
        Statement st = conn.createStatement();
        
        // Sepetteki her ürün için sipariş kaydediliyor
        for (int i = 0; i < model.size(); i++) {
            String item = model.elementAt(i);
            
            // JList'teki metin "->" ile bölünüyor, toplam fiyat alınıyor
            String[] parca = item.split("->");
            int ToplamMiktar = (int) Double.parseDouble(parca[1].trim());
            
            // Adet bilgisi "x" ile bölünerek alınıyor
            String[] YeniParca = parca[0].split("x");
            int adet = (int) Double.parseDouble(YeniParca[1]);
            
            // Sipariş veritabanına kaydediliyor
            String sql = String.format(
                "INSERT INTO siparisler(kullanici_id, cicek_id, adet, toplam_fiyat, teslimat_adresi, telefon, odeme_yontemi, hizli_teslimat) VALUES (%d, %d, %d, %d, '%s', '%s', '%s', %b)",
                kullaniciId, seciliCicekId, adet, (int) ToplamMiktar, adres, telefon, odeme, hizliTeslimat);
            st.executeUpdate(sql);
            
            // Sipariş verilen çiçeğin stoku güncelleniyor
            String StokGuncelle = String.format("UPDATE cicekler SET stok=stok - %d WHERE id=%d", adet, seciliCicekId);
            st.executeUpdate(StokGuncelle);
        }
        
        JOptionPane.showMessageDialog(null, "Sipariş başarıyla tamamlandı!", "Başarılı", JOptionPane.INFORMATION_MESSAGE);
        st.close();
        conn.close();
        
        // UserAnaSayfa'ya geri dönülüyor
        UserAnaSayfa anasayfa = new UserAnaSayfa(kullaniciAd, kullaniciId);
        anasayfa.setVisible(true);
        this.dispose();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "Hata: " + ex.getMessage(), "Hata!", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_SipariTamamlaButtonActionPerformed

