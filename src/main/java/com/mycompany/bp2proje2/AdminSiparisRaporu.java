    private void RaporuOlusturButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RaporuOlusturButtonActionPerformed
        // Tabloda seçili satırların indeksleri alınıyor
    int[] seciliSatirlar = Table.getSelectedRows();
    
    // Seçili satır var mı kontrolü
    if (seciliSatirlar.length == 0) {
        JOptionPane.showMessageDialog(null, "Lütfen en az bir sipariş seçiniz!", "Hata!", JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    // Rapor içeriği oluşturuluyor
    StringBuilder rapor = new StringBuilder();
    rapor.append("=== SİPARİŞ RAPORU ===\n");
    rapor.append("Tarih: ").append(new java.util.Date()).append("\n\n");
    
    int toplamCiro = 0;
    
    // Seçili her satır için rapor satırı oluşturuluyor
    for (int i : seciliSatirlar) {
        // JTable'dan ilgili kolonların değerleri alınıyor
        int kullaniciId = (int) Table.getValueAt(i, 1);
        String kullanici = Table.getValueAt(i, 2).toString();
        String cicek = Table.getValueAt(i, 3).toString();
        int adet = (int) Table.getValueAt(i, 4);
        int toplam = (int) Table.getValueAt(i, 5);
        String durum = Table.getValueAt(i, 6).toString();
        
        // Toplam ciro hesaplanıyor
        toplamCiro += toplam;
        
        // [kullanici_id:X] formatında satır rapora ekleniyor
        // Bu format User tarafından dosya okunurken filtreleme için kullanılıyor
        rapor.append(String.format("[kullanici_id:%d] %s - %s - %d adet - %d₺ - %s\n",
                kullaniciId, kullanici, cicek, adet, toplam, durum));
    }
    
    // Rapor özeti ekleniyor
    rapor.append("\nToplam Sipariş: ").append(seciliSatirlar.length).append("\n");
    rapor.append("Toplam: ").append(toplamCiro).append("₺\n");
    rapor.append("======================");
    
    // Rapor JTextArea'ya yazılıyor
    TextArea.setText(rapor.toString());
    }//GEN-LAST:event_RaporuOlusturButtonActionPerformed

