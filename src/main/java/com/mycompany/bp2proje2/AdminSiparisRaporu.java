    private void TabloYukle() {
       // JTable için boş bir model oluşturuluyor ve sütunlar ekleniyor
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("KullaniciID");
    model.addColumn("Kullanici");
    model.addColumn("Cicek");
    model.addColumn("Adet");
    model.addColumn("Toplam");
    model.addColumn("Durum");
    model.addColumn("Tarih");
    
    // Veritabanı bağlantısı açılıyor
    Connection conn = getConnection();
    if (conn == null) {
        return;
    }
    
    try {
        Statement st = conn.createStatement();
        
        // Tüm siparişler kullanıcı ve çiçek bilgileriyle birlikte JOIN ile çekiliyor
        // KullaniciID rapor oluşturmada kullanıcıya göre filtreleme için gerekli
        String sql = "SELECT s.id, s.kullanici_id, k.ad, k.soyad, c.ad as cicek_ad, s.adet, s.toplam_fiyat, s.durum, s.siparis_tarihi FROM siparisler s JOIN kullanicilar k ON s.kullanici_id = k.id JOIN cicekler c ON s.cicek_id = c.id";
        ResultSet rs = st.executeQuery(sql);
        
        // Her satır tabloya ekleniyor
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id"),
                rs.getInt("kullanici_id"),
                rs.getString("ad") + " " + rs.getString("soyad"),
                rs.getString("cicek_ad"),
                rs.getInt("adet"),
                rs.getInt("toplam_fiyat"),
                rs.getString("durum"),
                rs.getTimestamp("siparis_tarihi")
            });
        }
        
        // Model JTable'a bağlanıyor
        Table.setModel(model);
        st.close();
        conn.close();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "Hata: " + ex.getMessage(), "Hata!", JOptionPane.ERROR_MESSAGE);
    }
    }

