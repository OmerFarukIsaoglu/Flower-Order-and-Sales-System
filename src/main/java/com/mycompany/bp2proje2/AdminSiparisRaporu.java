    private void RaporuKaydetButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RaporuKaydetButtonActionPerformed
       // TextArea boş mu kontrolü, rapor oluşturulmadan kaydedilemez
    if (TextArea.getText().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Önce raporu oluşturunuz!", "Hata!", JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    try {
        // siparis_raporu.txt dosyası açılıyor, true ile mevcut içeriğin üzerine yazmadan sonuna ekleniyor
        BufferedWriter writer = new BufferedWriter(new FileWriter("siparis_raporu.txt", true));
        
        // TextArea'daki rapor içeriği dosyaya yazılıyor
        writer.write(TextArea.getText());
        
        // Bir sonraki rapor için boş satır ekleniyor
        writer.newLine();
        writer.close();
        
        JOptionPane.showMessageDialog(null, "Rapor kaydedildi!", "Başarılı", JOptionPane.INFORMATION_MESSAGE);
        
    } catch (IOException ex) {
        JOptionPane.showMessageDialog(null, "Dosya hatası: " + ex.getMessage(), "Hata!", JOptionPane.ERROR_MESSAGE);
    }
        
    }//GEN-LAST:event_RaporuKaydetButtonActionPerformed

